class Livros {
    constructor(nome, autor) {
        this.nome = nome;
        this.autor = autor;
    }
    getNome() {
        return this.nome;
    }
    setNome(nome) {
        this.nome = nome;
    }
    getAutor() {
        return this.autor;
    }
    setAutor(autor) {
        this.autor = autor;
    }
}
